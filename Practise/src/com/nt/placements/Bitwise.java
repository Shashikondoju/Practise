package com.nt.placements;

public class Bitwise {

	public static void main(String[] args) {
		int a=10;
		int b=a>>3;
		int c=a<<4;
		System.out.println(b+"  "+c);
		System.out.println(4&5);
		System.out.println(4|5);
		System.out.println(4^5);
		System.out.println(~254);
		System.out.println(15>>2);
		System.out.println(15<<2);
		System.out.println(~60);
	}

}
